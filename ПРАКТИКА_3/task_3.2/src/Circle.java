
public class Circle {
    private double x = 0;
    private double y = 0;
    private double center = 0;

    public Circle(){}

    public Circle(double x, double y) {
        this.x = x;
        this.y = y;
        this.center = center;
    }

    public String toString() {
        return "Point{" + "x=" + x + ", y=" + y + '}';
    }
}